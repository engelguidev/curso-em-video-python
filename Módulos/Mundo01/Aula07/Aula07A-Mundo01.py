n1=int(input('Digite um valor:'))
n2=int(input('Digite outro valor: '))
s=n1+n2
m=n1*n2
d=n1/n2
di=n1//n2
rd=n1%n2
e=n1**n2
print('A soma entre {} e {}, vale {}.'.format(n1, n2, s))
print('A multiplicação entre {} e {}, é {}.'.format(n1, n2, m))
print('A divisão entre {} e {}, é {}.'.format(n1, n2, d))
print('A Divisão inteira entre {} e {}, é {}.'.format(n1, n2, di))
print('O resto de divisão entre {} e {}, é {}.'.format(n1, n2, rd))
print('{} elevado por {}, resulta no valor de {}.'.format(n1, n2, e))
print('Sayonavara otário!')