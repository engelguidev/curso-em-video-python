algo=input('Digite algo: ')
print(f'Tipo primitivo:{algo}',type(algo))
print(f'É numérico? {algo.isnumeric()}')
print(f'É alfabeto? {algo.isalpha()}')
print(f'É alfanumérico? {algo.isalnum()}')
print(f'Está em caixa baixa? {algo.islower()}')
print(f'Está em caixa alta? {algo.isupper()}')
print(f'É apenas um espaço em branco? {algo.isspace()}')
print('Adeus seu broxa, finalizado.')








